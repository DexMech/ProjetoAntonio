package br.edu.Eventos.Modelos;

public class Item {
private String nome;
private String patSessp;
private String patDas;
private String modelo;
private String numeroSerial;
private String Observacao;


public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public String getPatSessp() {
	return patSessp;
}
public void setPatSessp(String patSessp) {
	this.patSessp = patSessp;
}
public String getPatDas() {
	return patDas;
}
public void setPatDas(String patDas) {
	this.patDas = patDas;
}
public String getModelo() {
	return modelo;
}
public void setModelo(String modelo) {
	this.modelo = modelo;
}
public String getNumeroSerial() {
	return numeroSerial;
}
public void setNumeroSerial(String numeroSerial) {
	this.numeroSerial = numeroSerial;
}
public String getObservacao() {
	return Observacao;
}
public void setObservacao(String observacao) {
	Observacao = observacao;
}

}
